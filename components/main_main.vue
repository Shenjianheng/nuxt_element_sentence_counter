<script>
import contentIntroduction from '../components/content_introduction.vue'
import selectLang from '../components/lang_select.vue'

export default {
  data() {
    return {
      textareaValue: '',
    }
  },

  components: {
    contentIntroduction,
    selectLang
  },

  methods: {
    /**
     * 清空文本内容
     */
    cleanText() {
      this.textareaValue = ''
    },
    /**
     * 复制文本内容
     */
    copytext() {
      if (this.textareaValue !== '') {
        // 将要复制的文本设置为input元素的值
        const inputText = document.createElement('textarea')
        // 将要复制的文本设置为input元素的值
        inputText.value = this.$refs.textareaInput.value
        // 添加到页面上临时显示
        document.body.appendChild(inputText)
        // 选中并复制文本
        inputText.select()
        document.execCommand('copy')
        // 移除临时显示的input元素
        document.body.removeChild(inputText)
        this.$message({
          message: 'Successfully copied the content to the clipboard.',
          type: 'success',
        })
      } else {
        this.$message({
          message:
            'Please type the text content you want to enter in the text box.',
          type: 'warning',
        })
      }
    },
  },

  computed: {
    /**
     * 动态监听字符的数量
     */
    charactersValue() {
      return this.textareaValue.length
    },
    /**
     * 动态监听单词的数量
     */
    wordsValue() {
      return this.textareaValue.split(/\s+/).filter(Boolean).length
    },
    /**
     * 动态监听空格符的数量
     */
    spacesValue() {
      return (this.textareaValue.match(/\s/g) || []).length
    },
    /**
     * 动态监听字母的数量
     */
    lettersValue() {
      return (this.textareaValue.match(/[a-zA-Z]/g) || []).length
    },
    /**
     * 动态监听不计算空格符的字符的数量
     */
    charactersWithoutSpacesValue() {
      return this.textareaValue.replace(/\s/g, '').length
    },
    /**
     * 动态监听句子的数量
     */
    sentencesValue() {
      return (this.textareaValue.match(/[.!?]+/g) || []).length
    },
    /**
     * 动态监听段落的数量
     */
    paragraphsValue() {
      // 根据句子前面是否有两个空格来判断是否增加段落数量
      let paragraphs = this.textareaValue.split(/\n+/).filter(Boolean)
      for (let i = 1; i < paragraphs.length; i++) {
        if (paragraphs[i - 1].trim().endsWith('  ')) {
          paragraphs[i] = paragraphs[i - 1] + paragraphs[i]
        }
      }
      return paragraphs.length
    },
  },
}
</script>

